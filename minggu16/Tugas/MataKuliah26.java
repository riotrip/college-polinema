package minggu16.Tugas;

public class MataKuliah26 {
    String kode;
    String matkul;
    String sks;

    public MataKuliah26() {
    }

    public MataKuliah26(String kode, String matkul, String sks) {
        this.kode = kode;
        this.matkul = matkul;
        this.sks = sks;
    }

    public String getKode() {
        return kode;
    }

    public String getMataKul() {
        return matkul;
    }

    public String getSks() {
        return sks;
    }
}